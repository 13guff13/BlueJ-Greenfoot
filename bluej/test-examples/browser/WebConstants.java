public interface WebConstants
{
  /** The version of this product which is displayed */
  public static final String BROWSER_VERSION = "0.02";

  /** The key under which the startup page is displayed in the 
      system properties */
  public static final String HOMEPAGE_PROPERTY = "java.browser.homepage";

  /** The key under which the start type is stored, this determines whether
      or not the homepage is loaded on startup */
  public static final String LOAD_HOMEPAGE_PROPERTY = "java.browser.homepage.start";

  /** The key under which the color of normal text is stored */
  public static final String TEXT_COLOR_PROPERTY = "java.browser.textcolor";

  /** The key under which the color of links are stored */
  public static final String LINK_COLOR_PROPERTY = "java.browser.linkcolor";

  public static final String BACKGROUND_COLOR_PROPERTY = "java.browser.backgroundcolor";


}
